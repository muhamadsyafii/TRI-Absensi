package dev.syafii.triabsensi.model;

public class AbsentIn {
    private String tgl_absensi;
    private String nik;
    private String waktu_in;

    public String getTgl_absensi() {
        return tgl_absensi;
    }

    public void setTgl_absensi(String tgl_absensi) {
        this.tgl_absensi = tgl_absensi;
    }

    public String getNik() {
        return nik;
    }

    public void setNik(String nik) {
        this.nik = nik;
    }

    public String getWaktu_in() {
        return waktu_in;
    }

    public void setWaktu_in(String waktu_in) {
        this.waktu_in = waktu_in;
    }
}
